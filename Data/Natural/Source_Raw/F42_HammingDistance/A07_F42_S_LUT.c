int HammingDistance(unsigned int x, unsigned int y) {
    unsigned int n = x ^ y;
    return table[n & 0xff] + table[(n >> 8) & 0xff] + table[(n >> 16) & 0xff] + table[(n >> 24) & 0xff];
}

